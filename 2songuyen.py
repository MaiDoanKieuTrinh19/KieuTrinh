a=int(input('Nhập số nguyên a='))
b=int(input('Nhập số nguyên b='))
print('Tổng hai số nguyên a, b là: ',a+b)
print('Hiệu hai số nguyên a, b là: ',a-b)
print('Tích hai số nguyên a, b là: ',a*b)
print('Thương 2 số nguyên a, b là: ',round(a/b,2))
print('Phép chia lấy dư của hai số nguyên a, b là: ',a%b)
print('Phép chia lấy nguyên của hai số nguyên a, b là: ',a//b)
